package cn.cqyt.day06.demo01;

public class Student {
    // 成员变量
    // 属性
    String name; // 名字
    int age; // 年龄

    // 成员方法，不要写static
    // 行为
    public void eat() {
        System.out.println("吃饭饭！");
    }

    public void study() {
        System.out.println("学习习！");
    }

    public void  sleep() {
        System.out.println("睡觉觉！");
    }
}